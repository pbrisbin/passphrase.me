-- | A key into the word list
--
-- Note: it takes @5@ @Int@s in the range @[1, 5]@ to build a @Key@. These
-- values can be determined dynamicaly from @'keyRange'@.
newtype Key = Key Int deriving (Eq, Ord, Show)

-- | The range within which @Key@ values can be constructed
keyRange :: (Int, Int)
keyRange = (1, 5)

-- | Build a @'Key'@ form a list of integers
--
-- Inappropriate values will result in @Nothing@.
fromList :: [Int] -> Maybe Key
fromList ints
    | length ints /= rangeSize keyRange = Nothing
    | not $ all (inRange keyRange) ints = Nothing
    | otherwise = Just $ Key $ read $ concatMap show ints

-- | Turn a stream of integers to a stream of @'Key'@s
--
-- Inappropriate values will result in fewer @Key@s (possibly none).
fromStream :: [Int] -> [Key]
fromStream [] = []
fromStream ints = consKey $ fromStream rest

  where
    consKey = maybe id (:) $ fromList batch

    (batch, rest) = splitAt (snd keyRange) $ filter (inRange keyRange) ints

